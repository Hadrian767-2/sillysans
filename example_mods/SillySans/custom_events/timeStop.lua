function onEvent(n,v1,v2)
    if n == 'timeStop' then
        if v1 == 'stop' then
            setProperty('boyfriend.skipDance',true)
            setProperty('gf.skipDance',true)
doTweenColor('bgname', 'bgname', '5A5A5A', 0.01, 'linear');
doTweenColor('bgname', 'bgname', '5A5A5A', 0.01, 'linear');

else v1 = 'resume'
 setProperty('boyfriend.skipDance',false)
            setProperty('gf.skipDance',false)
doTweenColor('bgname', 'bgname', 'FFFFFF', 0.01, 'linear');
doTweenColor('bgname', 'bgname', 'FFFFFF', 0.01, 'linear');
        	end
	end
end


